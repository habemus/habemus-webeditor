exports.format = {};

exports.format.workspacePreview = function (projectCode) {
  // for the service worker experimental version, we ignore the projectCode.
  // everything is within the same filesystem
  
  var baseURL;

  try {
    baseURL = window.location.protocol + '//' + window.location.host + '/preview';
  } catch (err) {
    console.log('window not available', err);
  }

  return baseURL;
};

module.exports = function (habemus, options) {
  
  var workspaceVersionCode = habemus.services.config.cloud.workspace.projectVersionCode;
  var projectLatestVersionCode = habemus.services.config.cloud.projectLatestVersion.code;
  
  var requiresUpdate = workspaceVersionCode !== projectLatestVersionCode;
  
  if (requiresUpdate) {
    var menuTrigger = document.querySelector('#menu-trigger');
    var warning = menuTrigger.querySelector('[icon="warning"]');
    
    menuTrigger.classList.add('has-notifications');
    warning.setAttribute('title', 'Your workspace is not up to date with the latest project version. Workspace at \'' + workspaceVersionCode + '\' and project at \'' + projectLatestVersionCode + '\'');
  }
  
};

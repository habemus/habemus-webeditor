// h-deps
const hUrls = require('h-urls');

// load url options from env
var urlOptions = {
  workspacePreviewHost: process.env.WORKSPACE_PREVIEW_HOST,
  uiWorkspaceBaseURL: process.env.UI_WORKSPACE_BASE_URL,
  websiteHost: process.env.WEBSITE_HOST,
};

if (!urlOptions.workspacePreviewHost) {
  throw new Error('process.env.WORKSPACE_PREVIEW_HOST must be set');
}

if (!urlOptions.uiWorkspaceBaseURL) {
  throw new Error('process.env.UI_WORKSPACE_BASE_URL must be set');
}

if (!urlOptions.websiteHost) {
  throw new Error('process.env.WEBSITE_HOST must be set');
}

/**
 * Helper that builds the urls from a series of configurations
 * @type {Object}
 */
var urls = hUrls(urlOptions);

module.exports = urls;
